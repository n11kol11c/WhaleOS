; ==============================================================================
;  BOOTLOADER PROJECT — UNDER ACTIVE DEVELOPMENT
; ==============================================================================
;  This bootloader is a work in progress and is intentionally kept extremely
;  minimalistic. The purpose of the project at this stage is educational and
;  experimental rather than production-ready. Only the most essential code paths
;  have been implemented, and many features that a full bootloader would normally
;  provide are absent by design.
;
;  The current goal is to build a clean, understandable foundation that can be
;  extended over time. As a result:
;
;    • The bootloader performs only the bare minimum initialization
;    • Advanced functionality (filesystem loading, multitasking, drivers, etc.)
;      has not yet been implemented
;    • Error handling remains simple or incomplete
;    • Comments and structure may change as the project evolves
;
;  This file should be viewed as a scaffold rather than a finished component.
;  Users cloning or studying this code should expect breaking changes, missing
;  features, and ongoing refactoring. The minimalistic nature is intentional:
;  it helps keep the control flow transparent and makes it easier to learn how
;  early-boot environments operate.
;
;  CONTRIBUTIONS AND FEEDBACK ARE WELCOME
;  -------------------------------------
;  If you experiment with this bootloader, find issues, or want to propose ideas,
;  feel free to open discussions or submit patches. Because the project is still
;  under construction, every improvement helps shape the direction of the final
;  design.
;
;  THANK YOU FOR CHECKING IT OUT
;  -----------------------------
